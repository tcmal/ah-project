def extend_key(key):
    # Expanded key = 240 bytes array
    extended_keys = np.zeros(240, dtype='int')

    # Set the first 32 bytes to the user-supplied key
    np.insert(extended_keys, 0, key)

    # Generate until our extended key is filled
    next_generate = len(key)
    next_block = 1
    while next_generate < len(extended_keys):
        a = np.copy(extended_keys[next_generate - 4:next_generate])

        ## Every 32 bytes (8 blocks), Do some extra calculations on the last 4 bytes (a) depending on which block we’re generating right now.
        if next_generate % 32 == 0:
            a = extend_key_core(a, next_block)

            next_block += 1

        ### Do an extra S-Box transform every 32 bytes
        if next_generate % 32 == 16:
            a = sbox(a)

        ### Set this 4-byte block to The 4 bytes in A xored with the 4 bytes starting 32 bytes before this block
        for i in range(0,4):
            extended_keys[next_generate] = a[i] ^ extended_keys[next_generate - 32]
            next_generate += 1
    return extended_keys

def extend_key_core(a, next_block):
    # Rotate a 1 byte to the left (circular)
    a = rotate_circular_left(a, 1)

    # Substitute each byte according to Rijndael’s S-Box.
    a = sbox(a)

    # XOR the first byte with rcon[i]
    a[0] = a[0] ^ RCON[next_block]

    return a

def createRoundKey(extended_keys, round_):
    # Get the bytes to use for the key
    arr = np.array(extended_keys[round_*16:round_*16 + 16])

    # Interpret it as a 4x4 matrix
    arr = np.reshape(arr, (4,4), order='F')
    return arr
